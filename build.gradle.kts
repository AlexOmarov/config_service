/* Custom image layering implementation. May be omitted due to existence of https://spring.io/blog/2020/01/27/creating-docker-images-with-spring-boot-2-3-0-m1 */
gradle.buildFinished {
	val srcDir = File("$buildDir/libs/${rootProject.name}-${version}.jar")
	if (srcDir.exists()) {
		println("Extracting deps")
		delete("build/deps")
		mkdir("build/deps")
		org.gradle.kotlin.dsl.support.unzipTo(file("build/deps"), srcDir)
	} else {
		println("No jar found, please build the project first")
	}
}

